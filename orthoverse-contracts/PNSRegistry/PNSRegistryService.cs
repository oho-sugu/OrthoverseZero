        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.Web3 web3, PNSRegistryDeployment pNSRegistryDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<PNSRegistryDeployment>().SendRequestAndWaitForReceiptAsync(pNSRegistryDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.Web3 web3, PNSRegistryDeployment pNSRegistryDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<PNSRegistryDeployment>().SendRequestAsync(pNSRegistryDeployment);
        }

        public static async Task<PNSRegistryService> DeployContractAndGetServiceAsync(Nethereum.Web3.Web3 web3, PNSRegistryDeployment pNSRegistryDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, pNSRegistryDeployment, cancellationTokenSource);
            return new PNSRegistryService(web3, receipt.ContractAddress);
        }

